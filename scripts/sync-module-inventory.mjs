#!/usr/bin/env node
/**
 * sync-module-inventory.mjs — zero-dependency generator.
 *
 * Reads the ACTUAL code in Med Spa App/ (package.json exports maps,
 * barrel re-exports, and supabase migrations) and injects an accurate
 * module inventory into two docs between paired marker comments:
 *
 *   1. MASTER_PROGRESS.md          (compact "Built Module Inventory")
 *   2. Phase & Build Docs/MODULES_LIBRARY.md  (fuller structure tree)
 *
 * Run:  node scripts/sync-module-inventory.mjs
 *       (or  pnpm sync-modules  from the Med Spa App workspace)
 *
 * Surrounding human prose is preserved — only the content between
 * <!-- BEGIN:MODULE_INVENTORY --> ... <!-- END:MODULE_INVENTORY --> is replaced.
 */

import { readFile, writeFile, readdir, stat } from 'node:fs/promises';
import { existsSync } from 'node:fs';
import { join, dirname, basename } from 'node:path';
import { fileURLToPath } from 'node:url';

const __dirname = dirname(fileURLToPath(import.meta.url));
const ROOT = join(__dirname, '..');
const APP = join(ROOT, 'Med Spa App');
const PACKAGES = join(APP, 'packages');
const MIGRATIONS = join(APP, 'supabase', 'migrations');

const MARK_BEGIN = '<!-- BEGIN:MODULE_INVENTORY -->';
const MARK_END = '<!-- END:MODULE_INVENTORY -->';
const GENERATED_TAG = '<!-- Generated by scripts/sync-module-inventory.mjs — do not edit by hand. Run `pnpm sync-modules`. -->';

// ─── Helpers ──────────────────────────────────────────────────────────

async function readJSON(p) {
  return JSON.parse(await readFile(p, 'utf8'));
}

/** Glob *.json in a directory (one level). */
async function listDir(p) {
  if (!existsSync(p)) return [];
  const entries = await readdir(p);
  const out = [];
  for (const e of entries) {
    const full = join(p, e);
    if ((await stat(full)).isDirectory()) out.push({ name: e, path: full });
  }
  return out;
}

/**
 * Extract sub-modules for a package.
 * Merges package.json `exports` subpaths with src/ directory scan
 * (catches modules present in the barrel but missing from exports map,
 *  e.g. core/clinics).
 */
async function extractSubModules(pkgDir) {
  const sub = new Set();
  const pkg = await readJSON(join(pkgDir, 'package.json'));

  // 1. From exports map (authoritative public API)
  if (pkg.exports && typeof pkg.exports === 'object') {
    for (const key of Object.keys(pkg.exports)) {
      if (key === '.') continue; // root entry, not a sub-module
      // normalize: ./auth -> auth, ./rbac/factory -> rbac (treat as sub of rbac)
      const clean = key.replace(/^\.\//, '');
      const top = clean.split('/')[0];
      sub.add(top);
    }
  }

  // 2. From src/ directory scan (catches barrel-only modules)
  const srcDir = join(pkgDir, 'src');
  if (existsSync(srcDir)) {
    for (const e of await readdir(srcDir)) {
      const idxTs = join(srcDir, e, 'index.ts');
      const idxTsx = join(srcDir, e, 'index.tsx');
      if (existsSync(idxTs) || existsSync(idxTsx)) {
        sub.add(e);
      }
    }
  }

  // Filter out non-module dirs/files: test dirs, shared types, internal helpers
  const EXCLUDE = new Set(['client']);
  const filtered = [...sub].filter((s) => !s.startsWith('__') && s !== 'types' && !EXCLUDE.has(s));
  return {
    name: pkg.name,
    subModules: filtered.sort(),
    description: pkg.description || '',
  };
}

/**
 * Count exported identifiers from a barrel file (src/index.ts).
 * Sums `export { a, b } from './x'` and `export type { T } from './x'`.
 */
async function countExports(pkgDir, subName) {
  const idxTs = join(pkgDir, 'src', subName, 'index.ts');
  const idxTsx = join(pkgDir, 'src', subName, 'index.tsx');
  let src = '';
  for (const p of [idxTs, idxTsx]) {
    if (existsSync(p)) { src = await readFile(p, 'utf8'); break; }
  }
  if (!src) return 0;
  let count = 0;
  // match identifiers inside export { ... } / export type { ... } blocks
  for (const m of src.matchAll(/export(?:\s+type)?\s*\{([^}]*)\}/g)) {
    const ids = m[1].split(',').map((s) => s.trim()).filter(Boolean);
    count += ids.length;
  }
  // named default-ish: export function X, export const Y, export class Z
  for (const m of src.matchAll(/export\s+(?:async\s+)?(?:function|const|class)\s+(\w+)/g)) {
    count += 1;
  }
  return count;
}

/** Read migrations: name + label (2nd SQL comment line if present). */
async function readMigrations() {
  if (!existsSync(MIGRATIONS)) return [];
  const files = (await readdir(MIGRATIONS))
    .filter((f) => f.endsWith('.sql'))
    .sort();
  const out = [];
  for (const f of files) {
    const txt = await readFile(join(MIGRATIONS, f), 'utf8');
    let label = '';
    const lines = txt.split('\n').map((l) => l.replace(/^--\s*/, '').trim()).filter(Boolean);
    // First line is usually the filename echo; use the 2nd as the description.
    if (lines.length >= 2) label = lines[1];
    else if (lines.length === 1) label = lines[0];
    out.push({ file: f.replace(/\.sql$/, ''), label });
  }
  return out;
}

// ─── Gather ───────────────────────────────────────────────────────────

async function gather() {
  const pkgDirs = await listDir(PACKAGES);
  const packages = [];
  for (const d of pkgDirs) {
    if (!existsSync(join(d.path, 'package.json'))) continue;
    const info = await extractSubModules(d.path);
    // enrich with export counts
    const detailed = [];
    for (const sm of info.subModules) {
      const n = await countExports(d.path, sm);
      detailed.push({ name: sm, exports: n });
    }
    packages.push({ ...info, dir: d.name, detailed });
  }
  // Integrations are nested under packages/integrations/*
  const integRoot = join(PACKAGES, 'integrations');
  if (existsSync(integRoot)) {
    const integDirs = await listDir(integRoot);
    const integrations = [];
    for (const d of integDirs) {
      if (!existsSync(join(d.path, 'package.json'))) continue;
      const p = await readJSON(join(d.path, 'package.json'));
      integrations.push(p.name.replace('@baseplate/', ''));
    }
    packages.push({
      name: '@baseplate/integrations',
      dir: 'integrations',
      subModules: integrations.sort(),
      detailed: integrations.sort().map((i) => ({ name: i, exports: 0 })),
      description: 'Third-party service integrations',
    });
  }
  const migrations = await readMigrations();
  return { packages, migrations };
}

// ─── Render: compact inventory (for MASTER_PROGRESS.md) ───────────────

function renderCompact({ packages, migrations }) {
  const lines = [];
  lines.push(GENERATED_TAG);
  lines.push('');

  let totalSubModules = 0;
  for (const pkg of packages) {
    if (pkg.dir === 'integrations') {
      lines.push(`### packages/integrations (${pkg.subModules.length} integrations)`);
      lines.push(pkg.subModules.join(', '));
    } else {
      const count = pkg.detailed.length;
      totalSubModules += count;
      const label = count > 0 ? `${count} modules` : 'standalone';
      lines.push(`### ${pkg.name} (${label})`);
      if (count > 0) {
        lines.push(pkg.detailed.map((d) => d.exports > 0 ? `${d.name} (${d.exports} exports)` : d.name).join(', '));
      } else if (pkg.description) {
        lines.push(`_${pkg.description}_`);
      }
    }
    lines.push('');
  }

  lines.push(`### supabase/migrations (${migrations.length} migrations)`);
  if (migrations.length) {
    const first = migrations[0].file;
    const last = migrations[migrations.length - 1].file;
    lines.push(`${first}–${last} — see Phase & Build Docs/MODULES_LIBRARY.md for the full table.`);
  }
  lines.push('');
  lines.push(`> **${packages.length} packages** · **${totalSubModules} sub-modules** · **${migrations.length} migrations** — regenerated from source on every \`pnpm sync-modules\`.`);

  return lines.join('\n');
}

// ─── Render: full structure + migration table (for MODULES_LIBRARY.md) ─

function renderFull({ packages, migrations }) {
  const lines = [];
  lines.push(GENERATED_TAG);
  lines.push('');
  lines.push('### Live Package Map (from `Med Spa App/packages/`)');
  lines.push('');
  lines.push('| Package | Sub-modules | Exports |');
  lines.push('|---------|-------------|---------|');

  let grandExports = 0;
  let grandSubs = 0;
  for (const pkg of packages) {
    if (pkg.dir === 'integrations') {
      const cell = pkg.subModules.join(', ') || '—';
      lines.push(`| \`${pkg.name}\` | ${cell} | — |`);
      grandSubs += pkg.subModules.length;
    } else {
      const cell = pkg.detailed.length
        ? pkg.detailed.map((d) => `\`${d.name}\``).join(', ')
        : '_(root only)_';
      const sum = pkg.detailed.reduce((a, d) => a + d.exports, 0);
      grandExports += sum;
      grandSubs += pkg.detailed.length;
      lines.push(`| \`${pkg.name}\` | ${cell} | ${sum} |`);
    }
  }
  lines.push(`| **Total** | **${grandSubs} sub-modules** | **${grandExports} exports** |`);
  lines.push('');

  lines.push('### Migration Catalog (from `Med Spa App/supabase/migrations/`)');
  lines.push('');
  lines.push('| # | Migration | Purpose |');
  lines.push('|---|-----------|---------|');
  for (const m of migrations) {
    lines.push(`| ${m.file.slice(0, 4)} | \`${m.file}\` | ${m.label || '—'} |`);
  }
  lines.push('');

  const missing = packages.filter((p) => p.dir !== 'integrations' && p.detailed.length === 0);
  if (missing.length) {
    lines.push('> ℹ️ Packages above with _(root only)_ expose a flat root barrel — add sub-module dirs + \`./name\` export entries to enrich the inventory.');
    lines.push('');
  }

  return lines.join('\n');
}

// ─── Inject between markers ───────────────────────────────────────────

function inject(doc, generated) {
  const begin = doc.indexOf(MARK_BEGIN);
  const end = doc.indexOf(MARK_END);
  if (begin === -1 || end === -1 || end < begin) {
    return { doc, ok: false };
  }
  const before = doc.slice(0, begin);
  const after = doc.slice(end + MARK_END.length);
  const out = before + MARK_BEGIN + '\n' + generated + '\n' + MARK_END + after;
  return { doc: out, ok: true };
}

// ─── Main ─────────────────────────────────────────────────────────────

async function main() {
  const data = await gather();

  const targets = [
    {
      path: join(ROOT, 'MASTER_PROGRESS.md'),
      render: renderCompact,
    },
    {
      path: join(ROOT, 'Phase & Build Docs', 'MODULES_LIBRARY.md'),
      render: renderFull,
    },
  ];

  let changed = 0;
  for (const t of targets) {
    if (!existsSync(t.path)) {
      console.warn(`⚠  Skipping (missing): ${t.path}`);
      continue;
    }
    const original = await readFile(t.path, 'utf8');
    const generated = t.render(data);
    const { doc: updated, ok } = inject(original, generated);
    if (!ok) {
      console.warn(`⚠  Skipping (no marker pair found): ${t.path}`);
      continue;
    }
    if (updated !== original) {
      await writeFile(t.path, updated, 'utf8');
      console.log(`✓  Updated: ${basename(t.path)}`);
      changed++;
    } else {
      console.log(`=  Already current: ${basename(t.path)}`);
    }
  }

  if (changed === 0) {
    console.log('\nAll inventories already in sync.');
  } else {
    console.log(`\nUpdated ${changed} doc(s).`);
  }

  // Report any modules lacking a prose entry (informational only)
  console.log('\nInventory summary:');
  for (const pkg of data.packages) {
    const label = pkg.dir === 'integrations'
      ? `${pkg.subModules.length} integrations`
      : `${pkg.detailed.length} modules`;
    console.log(`  ${pkg.name.padEnd(28)} ${label}`);
  }
  console.log(`  ${'migrations'.padEnd(28)} ${data.migrations.length}`);
}

main().catch((e) => {
  console.error(e);
  process.exit(1);
});
